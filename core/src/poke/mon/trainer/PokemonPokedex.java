package poke.mon.trainer;

public class PokemonPokedex {

	private int seen = 0;
	private int own = 0;
	
	public PokemonPokedex() {}
	
	public int getSeen() {
		return seen;
	}
	public void setSeen(int seen) {
		this.seen = seen;
	}
	public int getOwn() {
		return own;
	}
	public void setOwn(int own) {
		this.own = own;
	}
	
}
